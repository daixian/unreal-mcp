const TCHAR* ZSpaceAutoGenNodeComment = TEXT("MCP_ZSPACE_MINIMAL_AUTOGEN");

static UFunction* FindFunctionByCandidates(UClass* TargetClass, std::initializer_list<const TCHAR*> Candidates)
{
    if (!TargetClass)
    {
        return nullptr;
    }

    for (const TCHAR* Candidate : Candidates)
    {
        if (UFunction* Function = TargetClass->FindFunctionByName(*FString(Candidate)))
        {
            return Function;
        }
    }

    return nullptr;
}

static UEdGraphPin* FindPinByCandidates(UEdGraphNode* Node, std::initializer_list<const TCHAR*> Candidates, EEdGraphPinDirection Direction = EGPD_MAX)
{
    if (!Node)
    {
        return nullptr;
    }

    for (const TCHAR* Candidate : Candidates)
    {
        const FString CandidateName(Candidate);
        for (UEdGraphPin* Pin : Node->Pins)
        {
            if (!Pin)
            {
                continue;
            }

            if (Direction != EGPD_MAX && Pin->Direction != Direction)
            {
                continue;
            }

            const FString PinName = Pin->PinName.ToString();
            if (PinName == CandidateName || PinName.Equals(CandidateName, ESearchCase::IgnoreCase))
            {
                return Pin;
            }
        }
    }

    return nullptr;
}

static UEdGraphPin* FindFirstDataPin(UEdGraphNode* Node, EEdGraphPinDirection Direction)
{
    if (!Node)
    {
        return nullptr;
    }

    for (UEdGraphPin* Pin : Node->Pins)
    {
        if (!Pin || Pin->Direction != Direction)
        {
            continue;
        }

        if (Pin->PinType.PinCategory != UEdGraphSchema_K2::PC_Exec)
        {
            return Pin;
        }
    }

    return nullptr;
}

static bool ConnectPins(UEdGraphPin* SourcePin, UEdGraphPin* TargetPin)
{
    if (!SourcePin || !TargetPin)
    {
        return false;
    }

    const UEdGraphSchema_K2* K2Schema = GetDefault<UEdGraphSchema_K2>();
    if (K2Schema)
    {
        return K2Schema->TryCreateConnection(SourcePin, TargetPin);
    }

    SourcePin->MakeLinkTo(TargetPin);
    return true;
}

static bool ConnectNodePins(
    UEdGraphNode* SourceNode,
    std::initializer_list<const TCHAR*> SourcePinCandidates,
    UEdGraphNode* TargetNode,
    std::initializer_list<const TCHAR*> TargetPinCandidates)
{
    UEdGraphPin* SourcePin = FindPinByCandidates(SourceNode, SourcePinCandidates, EGPD_Output);
    if (!SourcePin)
    {
        SourcePin = FindFirstDataPin(SourceNode, EGPD_Output);
    }

    UEdGraphPin* TargetPin = FindPinByCandidates(TargetNode, TargetPinCandidates, EGPD_Input);
    return ConnectPins(SourcePin, TargetPin);
}

static bool ConnectExecPins(
    UEdGraphNode* SourceNode,
    std::initializer_list<const TCHAR*> SourcePinCandidates,
    UEdGraphNode* TargetNode,
    std::initializer_list<const TCHAR*> TargetPinCandidates)
{
    UEdGraphPin* SourceExecPin = FindPinByCandidates(SourceNode, SourcePinCandidates, EGPD_Output);
    if (!SourceExecPin)
    {
        SourceExecPin = FindPinByCandidates(SourceNode, { TEXT("Then"), TEXT("then"), TEXT("Execute"), TEXT("exec"), TEXT("Pressed") }, EGPD_Output);
    }

    UEdGraphPin* TargetExecPin = FindPinByCandidates(TargetNode, TargetPinCandidates, EGPD_Input);
    if (!TargetExecPin)
    {
        TargetExecPin = FindPinByCandidates(TargetNode, { TEXT("execute"), TEXT("Exec"), TEXT("In") }, EGPD_Input);
    }

    return ConnectPins(SourceExecPin, TargetExecPin);
}

static void SetPinDefaultValue(UEdGraphNode* Node, std::initializer_list<const TCHAR*> PinCandidates, const FString& Value)
{
    if (UEdGraphPin* Pin = FindPinByCandidates(Node, PinCandidates, EGPD_Input))
    {
        Pin->DefaultValue = Value;
    }
}

static void MarkNodeAsAutoGenerated(UEdGraphNode* Node)
{
    if (Node)
    {
        Node->NodeComment = ZSpaceAutoGenNodeComment;
    }
}

static void RemoveAutoGeneratedNodes(UEdGraph* Graph)
{
    if (!Graph)
    {
        return;
    }

    for (int32 Index = Graph->Nodes.Num() - 1; Index >= 0; --Index)
    {
        UEdGraphNode* Node = Graph->Nodes[Index];
        if (Node && Node->NodeComment == ZSpaceAutoGenNodeComment)
        {
            Graph->RemoveNode(Node);
        }
    }
}

static UK2Node_CallFunction* CreateFunctionNode(UEdGraph* Graph, UFunction* Function, const FVector2D& Position)
{
    if (!Graph || !Function)
    {
        return nullptr;
    }

    UK2Node_CallFunction* Node = NewObject<UK2Node_CallFunction>(Graph);
    Node->SetFromFunction(Function);
    Node->NodePosX = Position.X;
    Node->NodePosY = Position.Y;
    Graph->AddNode(Node, true);
    Node->CreateNewGuid();
    Node->PostPlacedNewNode();
    Node->AllocateDefaultPins();
    MarkNodeAsAutoGenerated(Node);
    return Node;
}

