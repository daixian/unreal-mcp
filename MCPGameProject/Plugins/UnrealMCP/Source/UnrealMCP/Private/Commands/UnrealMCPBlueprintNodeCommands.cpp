static bool EnsureBlueprintVariable(UBlueprint* Blueprint, const FName& VariableName, const FEdGraphPinType& VariableType)
{
    if (!Blueprint)
    {
        return false;
    }

    for (const FBPVariableDescription& ExistingVariable : Blueprint->NewVariables)
    {
        if (ExistingVariable.VarName == VariableName)
        {
            return true;
        }
    }

    return FBlueprintEditorUtils::AddMemberVariable(Blueprint, VariableName, VariableType);
}
