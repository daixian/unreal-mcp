/** @brief 自动生成节点的注释标记。 */
const TCHAR* AutoGeneratedNodeComment = TEXT("MCP_AUTOGEN");

/**
 * @brief 在节点中按候选名称查找引脚。
 * @param [in] Node 目标节点。
 * @param [in] Candidates 候选引脚名列表。
 * @param [in] Direction 引脚方向过滤。
 * @return UEdGraphPin* 找到的引脚，未找到返回 nullptr。
 */
static UEdGraphPin* FindPinByCandidates(UEdGraphNode* Node, std::initializer_list<const TCHAR*> Candidates, EEdGraphPinDirection Direction = EGPD_MAX)
{
    if (!Node)
    {
        return nullptr;
    }

    for (const TCHAR* Candidate : Candidates)
    {
        const FString CandidateName(Candidate);
        for (UEdGraphPin* Pin : Node->Pins)
        {
            if (!Pin)
            {
                continue;
            }

            if (Direction != EGPD_MAX && Pin->Direction != Direction)
            {
                continue;
            }

            const FString PinName = Pin->PinName.ToString();
            if (PinName == CandidateName || PinName.Equals(CandidateName, ESearchCase::IgnoreCase))
            {
                return Pin;
            }
        }
    }

    return nullptr;
}

/**
 * @brief 给节点打上自动生成标记。
 * @param [in] Node 目标节点。
 */
static void MarkNodeAsAutoGenerated(UEdGraphNode* Node)
{
    if (Node)
    {
        Node->NodeComment = AutoGeneratedNodeComment;
    }
}

/**
 * @brief 创建 Self 变量读取节点。
 * @param [in] Graph 目标图。
 * @param [in] VariableName 变量名。
 * @param [in] Position 节点位置。
 * @return UK2Node_VariableGet* 创建后的节点。
 */
static UK2Node_VariableGet* CreateSelfVariableGetNode(UEdGraph* Graph, const FString& VariableName, const FVector2D& Position)
{
    if (!Graph)
    {
        return nullptr;
    }

    UK2Node_VariableGet* Node = NewObject<UK2Node_VariableGet>(Graph);
    Node->VariableReference.SetSelfMember(*VariableName);
    Node->NodePosX = Position.X;
    Node->NodePosY = Position.Y;
    Graph->AddNode(Node, true);
    Node->CreateNewGuid();
    Node->PostPlacedNewNode();
    Node->AllocateDefaultPins();
    Node->ReconstructNode();
    MarkNodeAsAutoGenerated(Node);
    return Node;
}

